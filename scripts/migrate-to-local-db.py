async def migrate_table(
    cloud: asyncpg.Connection,
    local: asyncpg.Connection,
    table: str,
) -> tuple[int, int, int]:
    """Migrate a single table. Returns (total_cloud, copied, skipped)."""

    # Check if table exists on cloud
    cloud_exists = await cloud.fetchval(
        "SELECT EXISTS (SELECT 1 FROM information_schema.tables "
        "WHERE table_schema='public' AND table_name=$1)",
        table,
    )
    if not cloud_exists:
        print(f"  [{table}] does not exist on cloud -- skipping")
        return (0, 0, 0)

    # Create table locally if needed
    if not await table_exists_locally(local, table):
        ddl = await get_table_ddl(cloud, table)
        if not ddl:
            print(f"  [{table}] could not generate DDL -- skipping")
            return (0, 0, 0)
        await local.execute(ddl)
        # Also create indexes
        indexes = await get_indexes(cloud, table)
        for idx_stmt in indexes:
            try:
                await local.execute(idx_stmt)
            except Exception as e:
                print(f"  [{table}] index warning: {e}")
        print(f"  [{table}] created table locally")
    else:
        print(f"  [{table}] table already exists locally")

    # Get columns and PK
    columns = await get_column_names(cloud, table)
    pk_cols = await get_primary_key_columns(cloud, table)

    # Count cloud rows
    total_cloud = await cloud.fetchval(f'SELECT count(*) FROM "{table}"')
    if total_cloud == 0:
        print(f"  [{table}] 0 rows on cloud -- nothing to copy")
        return (0, 0, 0)

    # Count local rows before
    local_before = await local.fetchval(f'SELECT count(*) FROM "{table}"')

    # Fetch all rows from cloud
    rows = await cloud.fetch(f'SELECT * FROM "{table}"')

    # Build INSERT ... ON CONFLICT DO NOTHING
    col_list = ", ".join(f'"{c}"' for c in columns)
    placeholders = ", ".join(f"${i+1}" for i in range(len(columns)))

    if pk_cols:
        conflict_cols = ", ".join(f'"{c}"' for c in pk_cols)
        conflict_clause = f"ON CONFLICT ({conflict_cols}) DO NOTHING"
    else:
        # No PK -- can't do ON CONFLICT, use a workaround:
        # insert only, duplicates may appear if re-run (warn user)
        conflict_clause = ""
        print(f"  [{table}] WARNING: no primary key -- duplicates possible on re-run")

    insert_sql = (
        f'INSERT INTO "{table}" ({col_list}) VALUES ({placeholders}) {conflict_clause}'
    )

    # Batch insert
    batch_size = 500
    for i in range(0, len(rows), batch_size):
        batch = rows[i : i + batch_size]
        # Use executemany for speed
        await local.executemany(
            insert_sql,
            [tuple(row[c] for c in columns) for row in batch],
        )

    # Count local rows after
    local_after = await local.fetchval(f'SELECT count(*) FROM "{table}"')
    copied = local_after - local_before
    skipped = total_cloud - copied

    return (total_cloud, copied, skipped)


async def main():
    print("=" * 60)
    print("Glad Labs: Migrate Operational Tables to Local DB")
    print("=" * 60)
    print()

    # Connect
    print("Connecting to Cloud (Railway)...")
    cloud = await asyncpg.connect(CLOUD_DSN)
    print("Connecting to Local DB...")
    local = await asyncpg.connect(LOCAL_DSN)

    print()
    print("Checking for custom enum types...")
    await check_enum_types(cloud, local)
    print()

    results = []
    for table in TABLES_TO_MIGRATE:
        print(f"Migrating: {table}")
        t0 = time.time()
        total, copied, skipped = await migrate_table(cloud, local, table)
        elapsed = time.time() - t0
        results.append((table, total, copied, skipped, elapsed))
        if total > 0:
            print(
                f"  -> {total} cloud rows | {copied} copied | {skipped} skipped | {elapsed:.1f}s"
            )
        print()

    # Summary
    print("=" * 60)
    print(f"{'Table':<30} {'Cloud':>6} {'Copied':>7} {'Skipped':>8}")
    print("-" * 60)
    total_copied = 0
    total_skipped = 0
    for table, total, copied, skipped, elapsed in results:
        print(f"{table:<30} {total:>6} {copied:>7} {skipped:>8}")
        total_copied += copied
        total_skipped += skipped
    print("-" * 60)
    print(f"{'TOTAL':<30} {'':>6} {total_copied:>7} {total_skipped:>8}")
    print("=" * 60)
    print("\nMigration complete. Cloud data is untouched.")

    await cloud.close()
    await local.close()


if __name__ == "__main__":
    asyncio.run(main())
