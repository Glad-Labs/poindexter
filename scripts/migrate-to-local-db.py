async def get_table_ddl(conn: asyncpg.Connection, table: str) -> str | None:
    """Fetch the CREATE TABLE statement for a table from the cloud DB.

    Uses pg_dump-style reconstruction via pg_get_tabledef isn't available in
    vanilla PG, so we query information_schema + pg_indexes instead.
    """
    # Check table exists
    exists = await conn.fetchval(
        """
        SELECT EXISTS (
            SELECT 1 FROM information_schema.tables
            WHERE table_schema = 'public' AND table_name = $1
        )
        """,
        table,
    )
    if not exists:
        return None

    # Get column definitions
    cols = await conn.fetch(
        """
        SELECT column_name, data_type, udt_name,
               column_default, is_nullable,
               character_maximum_length, numeric_precision, numeric_scale
        FROM information_schema.columns
        WHERE table_schema = 'public' AND table_name = $1
        ORDER BY ordinal_position
        """,
        table,
    )
    if not cols:
        return None

    col_defs = []
    for c in cols:
        name = c["column_name"]
        # Use udt_name for user-defined / array types, data_type otherwise
        dtype = c["udt_name"]
        if dtype.startswith("_"):
            # Array type, e.g. _text -> text[]
            dtype = dtype[1:] + "[]"
        elif c["data_type"] == "character varying":
            length = c["character_maximum_length"]
            dtype = f"varchar({length})" if length else "text"
        elif c["data_type"] == "numeric" and c["numeric_precision"]:
            dtype = f"numeric({c['numeric_precision']},{c['numeric_scale'] or 0})"
        elif c["data_type"] in ("timestamp with time zone",):
            dtype = "timestamptz"
        elif c["data_type"] in ("timestamp without time zone",):
            dtype = "timestamp"
        elif c["data_type"] == "USER-DEFINED":
            # Already got udt_name above (e.g. vector, custom enum)
            pass

        nullable = "" if c["is_nullable"] == "YES" else " NOT NULL"
        col_default = c["column_default"] or ""

        # Convert nextval() serial columns to GENERATED BY DEFAULT AS IDENTITY
        if "nextval(" in col_default:
            col_defs.append(f'    "{name}" {dtype} GENERATED BY DEFAULT AS IDENTITY{nullable}')
        else:
            default = f" DEFAULT {col_default}" if col_default else ""
            col_defs.append(f'    "{name}" {dtype}{default}{nullable}')

    # Get primary key
    pk = await conn.fetch(
        """
        SELECT kcu.column_name
        FROM information_schema.table_constraints tc
        JOIN information_schema.key_column_usage kcu
            ON tc.constraint_name = kcu.constraint_name
            AND tc.table_schema = kcu.table_schema
        WHERE tc.table_schema = 'public'
            AND tc.table_name = $1
            AND tc.constraint_type = 'PRIMARY KEY'
        ORDER BY kcu.ordinal_position
        """,
        table,
    )
    if pk:
        pk_cols = ", ".join(f'"{r["column_name"]}"' for r in pk)
        col_defs.append(f"    PRIMARY KEY ({pk_cols})")

    ddl = f'CREATE TABLE IF NOT EXISTS "{table}" (\n'
    ddl += ",\n".join(col_defs)
    ddl += "\n);"

    return ddl


async def get_primary_key_columns(conn: asyncpg.Connection, table: str) -> list[str]:
    """Return list of PK column names for a table."""
    rows = await conn.fetch(
        """
        SELECT kcu.column_name
        FROM information_schema.table_constraints tc
        JOIN information_schema.key_column_usage kcu
            ON tc.constraint_name = kcu.constraint_name
            AND tc.table_schema = kcu.table_schema
        WHERE tc.table_schema = 'public'
            AND tc.table_name = $1
            AND tc.constraint_type = 'PRIMARY KEY'
        ORDER BY kcu.ordinal_position
        """,
        table,
    )
    return [r["column_name"] for r in rows]


async def get_indexes(conn: asyncpg.Connection, table: str) -> list[str]:
    """Return CREATE INDEX statements for non-PK indexes on a table."""
    rows = await conn.fetch(
        """
        SELECT indexdef
        FROM pg_indexes
        WHERE schemaname = 'public'
            AND tablename = $1
            AND indexname NOT IN (
                SELECT constraint_name FROM information_schema.table_constraints
                WHERE table_schema = 'public' AND table_name = $1
                    AND constraint_type = 'PRIMARY KEY'
            )
        """,
        table,
    )
    # Rewrite CREATE INDEX to CREATE INDEX IF NOT EXISTS
    result = []
    for r in rows:
        stmt = r["indexdef"]
        stmt = stmt.replace("CREATE INDEX ", "CREATE INDEX IF NOT EXISTS ", 1)
        stmt = stmt.replace("CREATE UNIQUE INDEX ", "CREATE UNIQUE INDEX IF NOT EXISTS ", 1)
        result.append(stmt + ";")
    return result


async def table_exists_locally(conn: asyncpg.Connection, table: str) -> bool:
    return await conn.fetchval(
        """
        SELECT EXISTS (
            SELECT 1 FROM information_schema.tables
            WHERE table_schema = 'public' AND table_name = $1
        )
        """,
        table,
    )


async def get_column_names(conn: asyncpg.Connection, table: str) -> list[str]:
    rows = await conn.fetch(
        """
        SELECT column_name FROM information_schema.columns
        WHERE table_schema = 'public' AND table_name = $1
        ORDER BY ordinal_position
        """,
        table,
    )
    return [r["column_name"] for r in rows]


async def check_enum_types(cloud: asyncpg.Connection, local: asyncpg.Connection):
    """Copy any custom enum types from cloud that don't exist locally."""
    enums = await cloud.fetch(
        """
        SELECT t.typname, string_agg(e.enumlabel, ',' ORDER BY e.enumsortorder) AS labels
        FROM pg_type t
        JOIN pg_enum e ON t.oid = e.enumtypid
        JOIN pg_namespace n ON t.typnamespace = n.oid
        WHERE n.nspname = 'public'
        GROUP BY t.typname
        """
    )
    for enum in enums:
        name = enum["typname"]
        labels = enum["labels"].split(",")
        exists = await local.fetchval(
            "SELECT EXISTS (SELECT 1 FROM pg_type WHERE typname = $1)", name
        )
        if not exists:
            labels_sql = ", ".join(f"'{l}'" for l in labels)
            await local.execute(f'CREATE TYPE "{name}" AS ENUM ({labels_sql});')
            print(f"  Created enum type: {name}")


