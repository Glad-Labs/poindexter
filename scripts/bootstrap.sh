#!/usr/bin/env bash
#
# Bootstrap Script — One-command setup for the AI Content Pipeline
#
# Usage:
#   bash scripts/bootstrap.sh
#
# What it does:
#   1. Checks prerequisites (Docker, Node.js, Python, Ollama)
#   2. Starts infrastructure (PostgreSQL + pgvector, Grafana)
#   3. Runs database migrations
#   4. Seeds default app_settings (all configurable, no hardcoding)
#   5. Pulls required Ollama models
#   6. Installs dependencies
#   7. Prints next steps
#
# After running this, you can:
#   - Start the backend: cd src/cofounder_agent && poetry run uvicorn main:app --port 8000
#   - Start the frontend: cd web/public-site && npm run dev
#   - View dashboards: http://localhost:3000 (admin/admin)

set -euo pipefail

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

info()  { echo -e "${BLUE}[INFO]${NC} $1"; }
ok()    { echo -e "${GREEN}[OK]${NC} $1"; }
warn()  { echo -e "${YELLOW}[WARN]${NC} $1"; }
fail()  { echo -e "${RED}[FAIL]${NC} $1"; exit 1; }

# ============================================================
# 1. Prerequisites
# ============================================================
info "Checking prerequisites..."

command -v docker >/dev/null 2>&1 || fail "Docker not found. Install: https://docker.com"
command -v node >/dev/null 2>&1 || fail "Node.js not found. Install: https://nodejs.org (v22+)"
command -v python3 >/dev/null 2>&1 || command -v python >/dev/null 2>&1 || fail "Python not found. Install: https://python.org (3.12+)"
command -v ollama >/dev/null 2>&1 || warn "Ollama not found. Install: https://ollama.com — needed for local LLM inference"

ok "Prerequisites found"

# ============================================================
# 2. Environment configuration
# ============================================================
if [ ! -f .env.local ]; then
    info "Creating .env.local from template..."
    if [ -f .env.example ]; then
        cp .env.example .env.local

        # Auto-generate required secrets
        _api_token=$(openssl rand -base64 32 2>/dev/null || python3 -c "import secrets; print(secrets.token_urlsafe(32))" 2>/dev/null || echo "change-me-$(date +%s)")
        _grafana_pw=$(openssl rand -base64 16 2>/dev/null || python3 -c "import secrets; print(secrets.token_urlsafe(16))" 2>/dev/null || echo "grafana-$(date +%s)")
        _pg_pw=$(openssl rand -base64 16 2>/dev/null || python3 -c "import secrets; print(secrets.token_urlsafe(16))" 2>/dev/null || echo "postgres-$(date +%s)")
        _pgadmin_pw=$(openssl rand -base64 16 2>/dev/null || python3 -c "import secrets; print(secrets.token_urlsafe(16))" 2>/dev/null || echo "pgadmin-$(date +%s)")

        # Write generated secrets into .env.local
        {
            echo ""
            echo "# Auto-generated by bootstrap.sh on $(date -Iseconds 2>/dev/null || date)"
            echo "API_TOKEN=${_api_token}"
            echo "GRAFANA_PASSWORD=${_grafana_pw}"
            echo "LOCAL_POSTGRES_PASSWORD=${_pg_pw}"
            echo "PGADMIN_PASSWORD=${_pgadmin_pw}"
        } >> .env.local

        ok ".env.local created with auto-generated secrets"
        info "Review .env.local and customize site settings before starting"
    else
        warn "No .env.example found — create .env.local manually"
    fi
else
    ok ".env.local already exists"
fi

# Source env vars for the rest of the script
if [ -f .env.local ]; then
    set -a
    source .env.local 2>/dev/null || true
    set +a
fi

# ============================================================
# 3. Start infrastructure
# ============================================================
info "Starting infrastructure (PostgreSQL + pgvector, Grafana)..."

docker compose -f docker-compose.local.yml up -d postgres-local grafana 2>/dev/null || \
    docker-compose -f docker-compose.local.yml up -d postgres-local grafana 2>/dev/null || \
    fail "Failed to start Docker containers. Is Docker running?"

# Wait for PostgreSQL to be ready
info "Waiting for PostgreSQL..."
for i in $(seq 1 30); do
    if docker exec poindexter-postgres-local pg_isready -U "${LOCAL_POSTGRES_USER:-poindexter}" -d "${LOCAL_POSTGRES_DB:-poindexter_brain}" >/dev/null 2>&1; then
        ok "PostgreSQL ready"
        break
    fi
    if [ "$i" -eq 30 ]; then
        fail "PostgreSQL didn't start in 30 seconds"
    fi
    sleep 1
done

# ============================================================
# 4. Install dependencies
# ============================================================
info "Installing Node.js dependencies..."
npm ci 2>/dev/null || npm install

info "Installing Python dependencies..."
cd src/cofounder_agent
if command -v poetry >/dev/null 2>&1; then
    poetry install --no-root
else
    pip install poetry
    poetry install --no-root
fi
cd ../..

ok "Dependencies installed"

# ============================================================
# 5. Run database migrations
# ============================================================
info "Running database migrations..."
cd src/cofounder_agent
poetry run python -c "
import asyncio
import asyncpg
import os

async def migrate():
    url = os.getenv('DATABASE_URL', 'postgresql://postgres:postgres@localhost:5432/glad_labs_dev')
    try:
        conn = await asyncpg.connect(url)
        print('Connected to production database — migrations will run on app startup')
        await conn.close()
    except Exception as e:
        print(f'Production DB not reachable (expected for local-only setup): {e}')
    print('Local brain DB initialized via Docker init.sql')

asyncio.run(migrate())
" 2>/dev/null || warn "Migration check skipped — will run on first backend start"
cd ../..

# ============================================================
# 6. Seed default settings
# ============================================================
info "Seeding default app_settings..."

# These are the configurable knobs — change them in the DB, not in code
PGPASSWORD="${LOCAL_POSTGRES_PASSWORD:-poindexter-brain-local}" psql -h localhost -p 5433 -U "${LOCAL_POSTGRES_USER:-poindexter}" -d "${LOCAL_POSTGRES_DB:-poindexter_brain}" -c "
