-- Image config
('enable_featured_image', 'true', 'image', 'Generate/search featured images', false),
('image_primary_source', 'pexels', 'image', 'Image source: pexels or ai_generation', false)
ON CONFLICT (key) DO NOTHING;
" 2>/dev/null && ok "Default settings seeded" || warn "Settings seed skipped (table may not exist yet — will be created on first backend start)"

# ============================================================
# 7. Detect hardware and recommend models
# ============================================================
info "Detecting hardware..."
if command -v python3 >/dev/null 2>&1; then
    python3 scripts/detect-hardware.py 2>/dev/null || python scripts/detect-hardware.py 2>/dev/null
elif command -v python >/dev/null 2>&1; then
    python scripts/detect-hardware.py 2>/dev/null
fi

# ============================================================
# 8. Pull Ollama models (based on hardware detection)
# ============================================================
if command -v ollama >/dev/null 2>&1; then
    # Check Ollama is actually running
    if curl -s http://localhost:11434/api/tags >/dev/null 2>&1; then
        ok "Ollama is running"
        info "Pulling minimum required models (this may take a while)..."
        ollama pull nomic-embed-text 2>/dev/null && ok "nomic-embed-text (embeddings)" || warn "Failed to pull nomic-embed-text"
        ollama pull qwen3:8b 2>/dev/null && ok "qwen3:8b (fast tasks, image decisions, SEO)" || warn "Failed to pull qwen3:8b"
        ollama pull gemma3:27b 2>/dev/null && ok "gemma3:27b (QA reviews, fallback)" || warn "Failed to pull gemma3:27b"
        info ""
        info "Minimum models pulled. For better writing quality, also pull a larger model:"
        info "  ollama pull qwen3:30b     # Good balance of speed and quality (18GB)"
        info "  ollama pull glm-4.7:9b    # Lighter alternative"
        info ""
        info "Or run: python scripts/detect-hardware.py --write-db"
        info "This auto-selects the best models for your GPU and saves to the database."
    else
        warn "Ollama is installed but not running. Start it with: ollama serve"
        warn "Then re-run this script to pull models, or pull manually:"
        warn "  ollama pull qwen3:8b gemma3:27b nomic-embed-text"
    fi
else
    warn "Ollama not installed — skip model pulls. Install from https://ollama.com"
    warn "Minimum models needed: qwen3:8b (fast tasks), gemma3:27b (QA reviews)"
fi

# ============================================================
# Done!
# ============================================================
echo ""
echo -e "${GREEN}========================================${NC}"
echo -e "${GREEN}  Setup complete!${NC}"
echo -e "${GREEN}========================================${NC}"
echo ""
echo "Next steps:"
echo "  1. Edit .env.local with your API keys and database URL"
echo "  2. Start the backend:"
echo "     cd src/cofounder_agent && poetry run uvicorn main:app --port 8000"
echo "  3. Start the frontend:"
echo "     cd web/public-site && npm run dev"
echo "  4. View Grafana dashboards:"
echo "     http://localhost:3000 (admin / poindexter)"
echo "  5. Create your first post:"
echo "     curl -X POST http://localhost:8000/api/tasks -H 'Content-Type: application/json' \\"
echo "       -d '{\"topic\": \"Your first AI-generated post\", \"category\": \"technology\"}'"
echo ""
echo "All settings are configurable in the database (app_settings table)."
echo "Change a value → pipeline uses it on the next run. No code changes needed."
echo ""
